    /* Start of login button */
    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {
        emailFieldActionPerformed(evt);
        passwordFieldActionPerformed(evt);

        final String fileLocation = "src/carrentalsystem/data/User.txt";

        int dataRow, passwordRow;
        // Check if the username and password are correct
        if (dataIO.rowNumber(loginDetails[0], 2, fileLocation, 5) != -1) {
            dataRow = dataIO.rowNumber(loginDetails[0], 2, fileLocation, 5); // row number of emails
            passwordRow = dataRow + 1; 
            String correctPassword = dataIO.readData(passwordRow, fileLocation); 
            if (correctPassword.equals(loginDetails[1])) {
                if (dataIO.readData(passwordRow + 1, fileLocation).equals("customer")) {
                    User.customer customer = new User().new customer(dataIO.readData(dataRow - 1, fileLocation), loginDetails[0], loginDetails[1]);
                    customer.setCustomer();
                    CustomerDashboard customerDashboard = new CustomerDashboard(customer);
                    pageSwitch.switchPage(this, customerDashboard);
                } else if (dataIO.readData(passwordRow + 1, fileLocation).equals("admin")) {
                    User.admin admin = new User().new admin(dataIO.readData(dataRow - 1, fileLocation), loginDetails[0], loginDetails[1]);
                    admin.setAdmin();
                    AdminDashboard adminDashboard = new AdminDashboard(admin);
                    pageSwitch.switchPage(this, adminDashboard);
                }   
            } else {
                messageHandling.incorrectPassword();
                emailField.setText("");
                passwordField.setText("");
            }
        }
    }
    /* End of login button */
    



